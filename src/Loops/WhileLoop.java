package Loops;

public class WhileLoop {
    public static void main(String[] args) {
        int i = 1;  //Initialization
        while(i<=10){ //condition
            System.out.println("Ansh"); //Work
            i++;   //Increment
        }

        //Infinite Loop
        while(2>1){
            System.out.println("ANSH");
        }
    }
}
