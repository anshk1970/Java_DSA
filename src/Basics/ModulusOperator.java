package Basics;

public class ModulusOperator {
    public static void main(String[] args) {
        int x = 7 % 3; //  1
        System.out.println(x);
        int y = -7 % 3; // -1
        System.out.println(y);
        int z = -7 % -3;  // -1
        System.out.println(z);
        int zz = 7 % -3; // 1
        System.out.println(zz);

    }
}
